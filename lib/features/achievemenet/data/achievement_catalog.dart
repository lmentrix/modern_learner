import 'package:modern_learner_production/features/achievemenet/model/achievement_model.dart';

class AchievementCatalog {
  AchievementCatalog._();

  static final List<AchievementDefinition> all = List.unmodifiable(
    _seeds.map(_definitionFromSeed),
  );

  static final Map<String, AchievementDefinition> byId = {
    for (final achievement in all) achievement.id: achievement,
  };

  static List<AchievementDefinition> byCategory(AchievementCategory category) {
    return all
        .where((achievement) => achievement.category == category)
        .toList(growable: false);
  }

  static Iterable<AchievementDefinition> matchingMetric(
    AchievementMetric metric,
  ) {
    return all.where((achievement) => achievement.metric == metric);
  }
}

class _AchievementSeed {
  const _AchievementSeed(
    this.id,
    this.level,
    this.title,
    this.description,
    this.metric,
    this.target,
    this.category,
    this.iconKey,
    this.colorHex,
    this.actionLabel,
  );

  final String id;
  final AchievementLevel level;
  final String title;
  final String description;
  final AchievementMetric metric;
  final int target;
  final AchievementCategory category;
  final String iconKey;
  final int colorHex;
  final String actionLabel;
}

AchievementDefinition _definitionFromSeed(_AchievementSeed seed) {
  return AchievementDefinition(
    id: seed.id,
    title: seed.title,
    description: seed.description,
    category: seed.category,
    level: seed.level,
    metric: seed.metric,
    target: seed.target,
    xpReward: _rewardByLevel[seed.level]!,
    iconKey: seed.iconKey,
    colorHex: seed.colorHex,
    actionLabel: seed.actionLabel,
  );
}

